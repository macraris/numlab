# Capitolo 9 - Il quadrato scritto a mano (senza loop)
# Quattro coppie di comandi: avanti + gira.
# Serve a far sentire alla bambina la ripetizione.

from turtle import *

title("02 - Quadrato a mano")
shape("turtle")
color("blue")
pensize(3)

forward(120)
right(90)
forward(120)
right(90)
forward(120)
right(90)
forward(120)
right(90)

done()
